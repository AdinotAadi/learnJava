package com.adinotaadi.Basics.Variables;

public class Variables {
    public static void main(String[] args) {
        // Following are the most common data types in Java.

        int age = 19; // 4 bytes long.
        float pi = 3.14f; // 4 bytes long.
        char gender = 'M'; // 2 bytes long.
        double longPi = 3.14956099; // 8 bytes long.
        long phnNumber = 7977990620L; // 8 bytes long.
        boolean isAdult = true; // 1 bit long.
        String name = "Aditya"; // Depends on the length of the string.
    }
}
